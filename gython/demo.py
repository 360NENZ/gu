#不得不做出的牺牲,但这是值得的!
from gython import *

#顾宇说要用improt导入库,于是就有了库,你可以使用gy.improt(packer)来引入库
gy.improt("random")

#顾宇说要用printll来打印信息,于是就有了信息,你可以使用gy.printll(printll)来打印内容
gy.printll(random.randint(0,10))

#我们不清楚15岁高中生的正常身体参数,但我们知道顾宇的,使用gy.number(0)也许我们能窥见一二
gy_weigh = gy.number(0)
gy.printll(gy_weigh)

#惑也,终不解矣,我们可以也许可以运用顾宇的方式来获取问题的答案,如果你也不知道的话,顾宇也许会很不开心哦,使用gy.gy.ask_wtf(wtf)可以获取输入信息
gy_asking_time = gy.ask_wtf("几点了？")
gy.printll(gy_asking_time)

#知人知面不知心,使用gy.gylang()我们就可以知道这个项目的信息了
gy.printll(gy.gylang())

#冬泳怪鸽曾说,面对恐惧的最好方法就是面对它,面对顾宇,最好的方法就是查清他的成分,使用gy.type()我们就可以知道顾宇的成分了
gy.printll(gy.type())

#钱不是万能的,没钱确实万万不能的,君子爱财,取之有道,给顾宇打钱会获得意想不到的收获,使用gy.pay(money)可以给顾宇打钱
gy.printll(gy.pay(50))
gy.printll(gy.pay(0.5))
